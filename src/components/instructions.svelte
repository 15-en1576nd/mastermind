<h1>Mastermind instructions</h1>
<p>
	The game is played with two players. The first player is the "mastermind" and the second player is
	the "guesser". The mastermind chooses a secret code consisting of four colors (red, green, blue,
	yellow). The guesser then tries to guess the secret code. The guesser has 12 guesses to guess the
	secret code. The guesser can guess a color by typing in the color's name (red, green, blue,
	yellow). The guesser can also guess a color by typing in the color's number (1, 2, 3, 4).
</p>
