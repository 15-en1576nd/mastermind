<p>
	The game is played with two players. The first player is the "mastermind" and the second player is
	the "guesser". The mastermind chooses a secret code consisting of four to eight emojis(depending
	on the difficulty). The guesser tries to guess the secret code. The guesser has to guess the
	secret code in 12 turns. Each turn the guesser can change any of the emojis in the secret code.
	After each guess, the guesser will be informed about the number of correct emojis and the number
	of correct positions. A green circle indicates a correct position and a red circle indicates a
	correct emoji.
</p>
